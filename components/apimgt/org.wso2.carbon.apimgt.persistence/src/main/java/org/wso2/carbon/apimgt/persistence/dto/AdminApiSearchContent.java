package org.wso2.carbon.apimgt.persistence.dto;

public class AdminApiSearchContent implements SearchContent {

    String id;
    String type = "API";
    String name;
    String transportType = "HTTP";
    String description;
    String context;
    String version;
    String provider;
    String status;
    String thumbnailUri;
    Boolean advertiseOnly;

    String keyManagerEntry;

    @Override
    public String getId() {
        return id;
    }

    @Override
    public String getType() {
        return type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTransportType() {
        return transportType;
    }

    public void setTransportType(String transportType) {
        this.transportType = transportType;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getContext() {
        return context;
    }

    public void setContext(String context) {
        this.context = context;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getProvider() {
        return provider;
    }

    public void setProvider(String provider) {
        this.provider = provider;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getThumbnailUri() {
        return thumbnailUri;
    }

    public void setThumbnailUri(String thumbnailUri) {
        this.thumbnailUri = thumbnailUri;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Boolean getAdvertiseOnly() {
        return advertiseOnly;
    }

    public void setAdvertiseOnly(Boolean advertiseOnly) {
        this.advertiseOnly = advertiseOnly;
    }

    public String getKeyManagerEntry() {
        return keyManagerEntry;
    }

    public void setKeyManagerEntry(String keyManagerEntry) {
        this.keyManagerEntry = keyManagerEntry;
    }
}
